<?php
    /*Uma constante é um identificador (nome) para um valor simples. 
    O valor não pode ser alterado durante o script

    Sintaxe: define(name, value, case-insensitive)

    Parâmetros:

    name : especifica o nome da constante
    value : especifica o valor da constante
    case-insensitive : especifica se o nome da constante não deve 
    diferenciar maiúsculas de minúsculas. O padrão é falso

    */

    define("GREETING", "Bem-vindo a escola");
    echo GREETING;



?>